#Disena un programa que, a partir del valor de la base y de la altura de un triangulo (3 y 5 metros, 
# respectivamente),muestre el valor de su area (en metros cuadrados).
#EJERCICIO CON ENTEROS

b=int(input("Introduce la base del triangulo: "))
a=int(input("Introduce la altura del triangulo: "))

area=(b*a)/2

print("El area del triangulo es",area)

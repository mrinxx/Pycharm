#Disena un algoritmo para calcular el area de un cırculo dado su radio. (Recuerda que el area de un cırculo 
# es π veces el cuadrado del radio.)

r=float(input("Introduce el radio del circulo: ")) #pido el radio
pi=3.14 #establezco pi como 3.14
area=pi*(r**2) #formula del area
print("El área del circulo de radio",r,"es:",area) #muestro el resultado